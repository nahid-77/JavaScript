//The assignment operator assigns value to the variables.

let a = 10;   // assigns value 10 to variable a
let b = 5;    // assigns value 5 to variable b
console.log(a);   // 10
console.log(b);   // 5

// Shorthand assignment operators combine arithmetic operations with assignment: 

// 1. Addition assignment operator (+=)
a += b;   //  a = a + b
console.log(a);   // 15

// 2. Subtraction assignment operator (-=)
a -= b;   //  a = a - b
console.log(a);   // 10

// 3. Multiplication assignment operator (*=)
a *= b;   //  a = a * b 
console.log(a);   // 50

// 4. Division assignment operator (/=)
a /= b;   //  a = a / b 
console.log(a);   // 10

// 5. Modulus assignment operator (%=)
a %= b;   //  a = a % b 
console.log(a);   // 0

// 6. Exponentiation assignment operator (**=)
a = 2;   
a **= b;  //  a = a ** b 
console.log(a);   // 32 