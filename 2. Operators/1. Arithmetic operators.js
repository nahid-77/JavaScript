let num1 = 10;
let num2 = 6;

// 1. Addition operator (+)
let sum = num1 + num2;
console.log(sum);    // 16

// 2. Subtraction operator (-)
let diff = num1 - num2;
console.log(diff);    // 4

// 3. Multiplication operator (*)
let prod = num1 * num2;
console.log(prod);    // 60

// 4. Division operator (/)
let quo = num1 / num2;
console.log(quo);    // 1.6666666666666667

// 5. Modulus operator (%)
let rem = num1 % num2;
console.log(rem);    // 4

// 6. Exponentiation operator (**)
let exp = num1 ** 2;
console.log(exp);      // 100

// 7. Increment operator (++)
num1++;
console.log(num1);    // 11

// 8. Decrement operator (--)
num2--;
console.log(num2);    // 5

//Pre increment/decrement
let x = 7;
let y = 5;

console.log(++x);    // 8
console.log(--y);    // 4
console.log(x);      // 8
console.log(y);      // 4

//Post increment/decrement
let a = 9;
let b = 6;
console.log(a++);      // 9
console.log(b--);      // 6

console.log(a);        // 10
console.log(b);        // 5