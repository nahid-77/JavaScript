// 1) Introduction:

// Variables = Data Containers
// JavaScript variables are containers for data.

// JavaScript variables can be declared in 4 ways:
// Modern JavaScript: 
// 1. Using let         2.Using const
// Older JavaScript:
//3. Using var (Not Recommended)   4.Automatically (Not Recommended)


// 2) Example using let:
let x = 1;
let y = 2;
let z = x + y; 
console.log(z); // 3


// 3) Example using const:
const x1 = 4;
const y1 = 5;
const z1 = x1 + y1;
console.log(z1); // 9


// 4) Example using var (Not Recommended):
var x2 = 10;
var y2 = 11;
var z2 = x2 + y2;
console.log(z2); // 21


// 4) Example using a variable Automatically (Not Recommended):
x3 = 22;
y3 = 23;
z3 = x3 + y3;
console.log(z3); // 45