// Array literals are a way to create arrays in JavaScript
// using a concise syntax. They are defined using square brackets []

let colors = ['orange', 'red', 'aqua', 'green', 'blue'];
console.log(colors);   //  ['orange', 'red', 'aqua', 'green', 'blue']

console.log(colors[1]);  // red
console.log(colors[3])   // green
