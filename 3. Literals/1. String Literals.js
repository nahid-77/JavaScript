// String literals in JavaScript are sequences of characters enclosed within single ('') or double ("") quotation marks.
let myName = "Nahid Hosen";
let message = "Hello World!";

console.log(myName);  //  Nahid Hosen
console.log(message); //  Hello, World!

// Concatenation of strings :
let firstName = "Nahid";
let middleName = "Hosen";
let lastName = "Tuhin";
let fullName = firstName + " " + middleName + " " + lastName;

console.log(fullName);     //  Nahid Hosen Tuhin
