// Ternary operators provide a shorthand way to write conditional statements in JavaScript.
// They are a more concise alternative to traditional if-else statements and are often used for simple conditions.

// Syntax:
// (condition) ? expression1 : expression2

// Example: 
age = 15;
let text = (age < 18) ? "Not an Adult" : "Adult";
console.log(text);
// Output : Not an Adult

