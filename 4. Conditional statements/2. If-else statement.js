// In JavaScript, an 'if-else' statement is a conditional statement that allows us
// to execute one block of code if a specified condition evaluates to true,
// and another block of code if the condition evaluates to false.

// Syntax:
// if (condition) {
//      Code block to be executed if condition is true
// } else {
//      Code block to be executed if condition is false
// }

// Example:
let age = 22;

if(age >= 18){
    console.log("I can Drive");
}else{
    console.log("I cannot Drive");
}
// Output : I cannot Drive